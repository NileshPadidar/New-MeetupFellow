package com.connect.meetupsfellow.mvp.view.model;

public class GroupModal {

    String admin, adminId, imgUrl, address, search, groupName, time, delete, lstMsg, lstMTime;

    public GroupModal() {

    }

    public String getAdmin() {
        return admin;
    }

    public void setAdmin(String admin) {
        this.admin = admin;
    }

    public String getAdminId() {
        return adminId;
    }

    public void setAdminId(String adminId) {
        this.adminId = adminId;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getSearch() {
        return search;
    }

    public void setSearch(String search) {
        this.search = search;
    }

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getDelete() {
        return delete;
    }

    public void setDelete(String delete) {
        this.delete = delete;
    }

    public String getLstMsg() {
        return lstMsg;
    }

    public void setLstMsg(String lstMsg) {
        this.lstMsg = lstMsg;
    }

    public String getLstMTime() {
        return lstMTime;
    }

    public void setLstMTime(String lstMTime) {
        this.lstMTime = lstMTime;
    }
}
